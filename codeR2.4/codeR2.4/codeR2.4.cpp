/*
Дан циклический двусвязный линейный список и указатель первый
элемент этого списка. Необходимо удалить в списке все элементы, у которых правый и
левый сосед совпадают. Если таких элементов нет, то оставить список без изменений.
Первый и последний элементы считать соседями. В результате вернуть ссылку на
последний элемент полученного списка.
Все динамические структуры данных реализовывать через классы. Не использовать STL.
Для каждой динамической структуры должен быть предусмотрен стандартный
набор методов - добавления/удаления/вывода элементов. Во всех задачах обязательно наличие
дружественного интерфейса. Ввод данных с клавиатуры.
*/
#include "Header.h"
#include <iostream>

//FIX_ME: использовался using namespace std
//using namespace std;

int main()
{
    std::setlocale(LC_ALL, "rus");

    //FIX_ME: имя переменной list в PascalCase
    //DoubleList list;
    DoubleList List;

    int n;
    int value;

    // Ввод количества элементов с проверкой
    std::cout << "Сколько чисел надо ввести: ";
    std::cin >> n;
    if (n <= 0)
    {
        std::cout << "Ошибка: количество элементов должно быть положительным." << std::endl;
        return 1;
    }

    // Ввод элементов списка с проверкой корректности
    std::cout << "Введите числа:" << std::endl;
    for (int i = 0; i < n; i++)
    {
        std::cin >> value;
        if (std::cin.fail())
        {
            std::cout << "Ошибка: введено не число!" << std::endl;
            return 1;
        }

        //FIX_ME: метод push_back приведен к PascalCase
        //List.push_back(value);
        List.PushBack(value);
    }

    // Вывод исходного списка
    std::cout << "Исходный список: ";
    List.PrintList();

    //FIX_ME: метод removeSides приведен к PascalCase
    //DoubleList::Node* tail = List.removeSides();
    // Удаление элементов, у которых левый и правый сосед совпадают
    DoubleList::Node* Tail = List.RemoveSides();

    // Вывод обработанного списка
    std::cout << "Обработанный список: ";
    List.PrintList();

    // Вывод последнего элемента после обработки
    if (Tail)
    {
        std::cout << "Последний элемент: " << Tail->Data << std::endl;
    }

    return 0;
}