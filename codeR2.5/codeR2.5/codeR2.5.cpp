/* Элементами контейнеров являются целые числа. Для
заполнения контейнера использовать итератор и конструктор соответствующего контейнера,
для вывода элементов использовать итератор (для вывода элементов в обратном порядке
использовать обратные итераторы, возвращаемые функциями-членами rbegin и rend)
Обязательно наличие дружественного интерфейса. Ввод данных организовать
разными способами (с клавиатуры, рандом, из файла)

Даны списки L1 и L2; список L1 имеет нечетное количество элементов.
Переместить средний элемент списка L1 в конец списка L2. Использовать один вызов
функции-члена splice.

*/
#include "Header.h"
#include <iostream>
#include <list>

int main()
{
    std::setlocale(LC_ALL, "Russian");

    int N1, N2;
    std::list<int> L1;
    std::list<int> L2;

    // Ввод размера списка L1
    std::cout << "Введите размерность списка L1 (нечетное число): ";
    std::cin >> N1;

    if (N1 <= 0 || N1 % 2 == 0)
    {
        std::cout << "Ошибка: размер списка L1 должен быть положительным и нечетным." << std::endl;
        return 1;
    }

    // Ввод списка L1
    ListOperations::Input(L1, N1);

    // Ввод размера списка L2
    std::cout << "Введите размерность списка L2 (>= 0): ";
    std::cin >> N2;

    if (N2 < 0)
    {
        std::cout << "Ошибка: размер списка L2 не может быть отрицательным." << std::endl;
        return 1;
    }

    // Ввод списка L2
    ListOperations::Input(L2, N2);

    // Вывод исходных списков
    std::cout << "Исходные списки:" << std::endl;
    std::cout << "L1: ";
    ListOperations::Print(L1);
    std::cout << "L2: ";
    ListOperations::Print(L2);

    // Перемещение среднего элемента списка L1 в конец L2
    ListOperations::MoveMiddleToEnd(L1, L2);

    // Вывод списков после изменения
    std::cout << "Списки после перемещения:" << std::endl;
    std::cout << "L1: ";
    ListOperations::Print(L1);
    std::cout << "L2: ";
    ListOperations::Print(L2);

    return 0;
}